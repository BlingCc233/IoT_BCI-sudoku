package main

import (
	"encoding/json"
	"flag"
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strings"
	"time"

	"github.com/BlingCc233/IoT_BCI-sudoku/internal/bench"
)

type evidenceReport struct {
	GeneratedAt time.Time `json:"generated_at"`
	RepoRoot    string    `json:"repo_root"`

	CoreSourceBytes int64 `json:"core_source_bytes"`

	Scenarios []scenarioReport `json:"scenarios"`
}

type scenarioReport struct {
	Name    string               `json:"name"`
	Metrics bench.ProtocolResult `json:"metrics"`
}

func main() {
	var (
		benchPath    = flag.String("bench", "", "optional: bench.json from cmd/iotbci-bench")
		evidencePath = flag.String("evidence", "", "optional: evidence_out/evidence.json from cmd/iotbci-evidence")
		outDir       = flag.String("out_dir", "tex/generated", "output directory (LaTeX snippets)")
	)
	flag.Parse()

	if strings.TrimSpace(*benchPath) == "" && strings.TrimSpace(*evidencePath) == "" {
		fatal(fmt.Errorf("at least one of -bench or -evidence is required"))
	}

	if err := os.MkdirAll(*outDir, 0o755); err != nil {
		fatal(err)
	}

	now := time.Now().Format(time.RFC3339)

	if strings.TrimSpace(*benchPath) != "" {
		b, err := os.ReadFile(*benchPath)
		if err != nil {
			fatal(err)
		}
		var rep bench.Report
		if err := json.Unmarshal(b, &rep); err != nil {
			fatal(err)
		}
		results := sortedResults(rep.Results)

		writeFile(filepath.Join(*outDir, "bench_table.tex"), renderBenchTable(now, "bench", results))
		writeFile(filepath.Join(*outDir, "bench_overhead_plot.tex"), renderBarPlot(now, "Wire Overhead Ratio (wire/payload)", "overhead", results))
		writeFile(filepath.Join(*outDir, "bench_rtt_plot.tex"), renderBarPlot(now, "Average RTT (ms)", "avg_rtt_ms", results))
		writeFile(filepath.Join(*outDir, "bench_mem_plot.tex"), renderBarPlot(now, "Peak Heap In-Use (MB)", "peak_heap_inuse_mb", results))
		writeFile(filepath.Join(*outDir, "bench_throughput_plot.tex"), renderBarPlot(now, "Wire Throughput (MB/s)", "wire_throughput_mb_s", results))
		writeFile(filepath.Join(*outDir, "bench_entropy_plot.tex"), renderBarPlot(now, "Wire Entropy (bits/byte)", "wire_entropy", results))
		writeFile(filepath.Join(*outDir, "bench_ascii_plot.tex"), renderBarPlot(now, "Wire ASCII Ratio", "wire_ascii_ratio", results))
		writeFile(filepath.Join(*outDir, "bench_write_size_plot.tex"), renderDistLinePlot(now, "Write size distribution (normalized)", "log2(bytes)", "write_size", results))
		writeFile(filepath.Join(*outDir, "bench_write_iat_plot.tex"), renderDistLinePlot(now, "Write inter-arrival distribution (normalized)", "log2(ms)", "write_iat", results))
		writeFile(filepath.Join(*outDir, "bench_figures.tex"), renderFigures(now, "bench"))
	}

	if strings.TrimSpace(*evidencePath) != "" {
		b, err := os.ReadFile(*evidencePath)
		if err != nil {
			fatal(err)
		}
		var rep evidenceReport
		if err := json.Unmarshal(b, &rep); err != nil {
			fatal(err)
		}

		results := make([]bench.ProtocolResult, 0, len(rep.Scenarios))
		for _, sc := range rep.Scenarios {
			r := sc.Metrics
			if r.Name == "" {
				r.Name = sc.Name
			}
			results = append(results, r)
		}
		results = sortedResults(results)

		writeFile(filepath.Join(*outDir, "evidence_table.tex"), renderBenchTable(now, "evidence", results))
		writeFile(filepath.Join(*outDir, "evidence_overhead_plot.tex"), renderBarPlot(now, "Wire Overhead Ratio (wire/payload)", "overhead", results))
		writeFile(filepath.Join(*outDir, "evidence_rtt_plot.tex"), renderBarPlot(now, "Average RTT (ms)", "avg_rtt_ms", results))
		writeFile(filepath.Join(*outDir, "evidence_mem_plot.tex"), renderBarPlot(now, "Peak Heap In-Use (MB)", "peak_heap_inuse_mb", results))
		writeFile(filepath.Join(*outDir, "evidence_throughput_plot.tex"), renderBarPlot(now, "Wire Throughput (MB/s)", "wire_throughput_mb_s", results))
		writeFile(filepath.Join(*outDir, "evidence_entropy_plot.tex"), renderBarPlot(now, "Wire Entropy (bits/byte)", "wire_entropy", results))
		writeFile(filepath.Join(*outDir, "evidence_ascii_plot.tex"), renderBarPlot(now, "Wire ASCII Ratio", "wire_ascii_ratio", results))
		writeFile(filepath.Join(*outDir, "evidence_write_size_plot.tex"), renderDistLinePlot(now, "Write size distribution (normalized)", "log2(bytes)", "write_size", results))
		writeFile(filepath.Join(*outDir, "evidence_write_iat_plot.tex"), renderDistLinePlot(now, "Write inter-arrival distribution (normalized)", "log2(ms)", "write_iat", results))
		writeFile(filepath.Join(*outDir, "evidence_figures.tex"), renderFigures(now, "evidence"))
	}
}

func sortedResults(in []bench.ProtocolResult) []bench.ProtocolResult {
	out := append([]bench.ProtocolResult(nil), in...)
	sort.Slice(out, func(i, j int) bool { return out[i].Name < out[j].Name })
	return out
}

func renderBenchTable(genAt, dataset string, results []bench.ProtocolResult) string {
	var b strings.Builder
	b.WriteString("% Auto-generated by cmd/iotbci-texgen at " + genAt + "\n")
	b.WriteString("\\begin{table}[htbp]\n\\centering\n")
	b.WriteString(fmt.Sprintf("\\caption{Protocol comparison results (%s, auto-generated).}\n", latexEscape(dataset)))
	b.WriteString(fmt.Sprintf("\\label{tab:iotbci-%s-metrics}\n", latexEscape(dataset)))
	b.WriteString("\\begin{tabular}{lrrrrrrrr}\n")
	b.WriteString("\\toprule\n")
	b.WriteString("Protocol & Overhead(x) & Avg RTT(ms) & P95 RTT(ms) & Wire(KB) & PeakHeap(MB) & WireTP(MB/s) & Entropy & ASCII\\\\\n")
	b.WriteString("\\midrule\n")
	for _, r := range results {
		wireKB := float64(r.WireBytesTotal) / 1024.0
		heapMB := float64(r.PeakHeapInuseBytes) / 1024.0 / 1024.0
		wireTPMBps := r.WireThroughputBps / 1024.0 / 1024.0
		b.WriteString(fmt.Sprintf(
			"%s & %.2f & %.4f & %.4f & %.1f & %.2f & %.2f & %.3f & %.3f\\\\\n",
			latexEscape(r.Name),
			r.OverheadRatio,
			r.AvgRTTMillis,
			r.P95RTTMillis,
			wireKB,
			heapMB,
			wireTPMBps,
			r.WireEntropy,
			r.WireASCIIRatio,
		))
	}
	b.WriteString("\\bottomrule\n")
	b.WriteString("\\end{tabular}\n")
	b.WriteString("\\end{table}\n")
	return b.String()
}

