func renderBarPlot(genAt, title, metric string, results []bench.ProtocolResult) string {
	var (
		coords strings.Builder
		labels []string
	)
	for _, r := range results {
		labels = append(labels, latexEscape(r.Name))
		var v float64
		switch metric {
		case "overhead":
			v = r.OverheadRatio
		case "avg_rtt_ms":
			v = r.AvgRTTMillis
		case "peak_heap_inuse_mb":
			v = float64(r.PeakHeapInuseBytes) / 1024.0 / 1024.0
		case "wire_throughput_mb_s":
			v = r.WireThroughputBps / 1024.0 / 1024.0
		case "wire_entropy":
			v = r.WireEntropy
		case "wire_ascii_ratio":
			v = r.WireASCIIRatio
		default:
			v = 0
		}
		coords.WriteString(fmt.Sprintf("(%s,%.6f) ", latexEscape(r.Name), v))
	}

	// PGFPlots bar chart (symbolic x coords).
	return fmt.Sprintf(`%% Auto-generated by cmd/iotbci-texgen at %s
\begin{tikzpicture}
\begin{axis}[
  ybar,
  grid=major,
  width=\linewidth,
  height=6cm,
  ymin=0,
  title={%s},
  symbolic x coords={%s},
  xtick=data,
  x tick label style={rotate=45,anchor=east},
  bar width=7pt,
  nodes near coords,
  nodes near coords align={vertical},
]
\addplot+[fill=blue!55] coordinates {%s};
\end{axis}
\end{tikzpicture}
`,
		genAt,
		latexEscape(title),
		strings.Join(labels, ","),
		coords.String(),
	)
}

func pickSeries(results []bench.ProtocolResult) []bench.ProtocolResult {
	wants := []string{
		"iotbci-sudoku-pure",
		"iotbci-sudoku-packed",
		"pure-aead",
		"coap-udp",
		"dtls",
		"mqtt",
	}

	var out []bench.ProtocolResult
	for _, needle := range wants {
		for _, r := range results {
			if strings.Contains(r.Name, needle) {
				out = append(out, r)
				break
			}
		}
	}
	if len(out) > 0 {
		return out
	}
	if len(results) > 4 {
		return results[:4]
	}
	return results
}

func renderDistLinePlot(genAt, title, xlabel, metric string, results []bench.ProtocolResult) string {
	series := pickSeries(results)
	maxBin := 0
	for _, r := range series {
		var bins [32]uint64
		switch metric {
		case "write_size":
			bins = r.WireWriteSizeBinsLog2
		case "write_iat":
			bins = r.WireWriteInterArrivalMsBinsL2
		default:
			continue
		}
		for i := 31; i >= 0; i-- {
			if bins[i] != 0 && i > maxBin {
				maxBin = i
				break
			}
		}
	}
	if maxBin > 16 {
		maxBin = 16
	}
	if maxBin < 2 {
		maxBin = 8
	}

	var plots strings.Builder
	for _, r := range series {
		var bins [32]uint64
		switch metric {
		case "write_size":
			bins = r.WireWriteSizeBinsLog2
		case "write_iat":
			bins = r.WireWriteInterArrivalMsBinsL2
		default:
			continue
		}

		var total uint64
		for i := 0; i <= maxBin; i++ {
			total += bins[i]
		}

		var coords strings.Builder
		for i := 0; i <= maxBin; i++ {
			var frac float64
			if total > 0 {
				frac = float64(bins[i]) / float64(total)
			}
			coords.WriteString(fmt.Sprintf("(%d,%.6f) ", i, frac))
		}

		plots.WriteString(fmt.Sprintf("\\addplot+[mark=*,mark size=1pt] coordinates {%s};\n", coords.String()))
		plots.WriteString(fmt.Sprintf("\\addlegendentry{%s}\n", latexEscape(r.Name)))
	}

	return fmt.Sprintf(`%% Auto-generated by cmd/iotbci-texgen at %s
\begin{tikzpicture}
\begin{axis}[
  grid=major,
  width=\linewidth,
  height=6cm,
  ymin=0,
  ymax=1,
  title={%s},
  xlabel={%s},
  ylabel={normalized count},
  legend style={font=\small, at={(0.5,-0.25)}, anchor=north, legend columns=2},
]
%% Each series is normalized by its own total count.
%% maxBin=%d, metric=%s
%s
\end{axis}
\end{tikzpicture}
`,
		genAt,
		latexEscape(title),
		latexEscape(xlabel),
		maxBin,
		latexEscape(metric),
		plots.String(),
	)
}

func latexEscape(s string) string {
	r := strings.NewReplacer(
		"\\", "\\textbackslash{}",
		"_", "\\_",
		"%", "\\%",
		"&", "\\&",
		"#", "\\#",
		"{", "\\{",
		"}", "\\}",
		"$", "\\$",
		"~", "\\textasciitilde{}",
		"^", "\\textasciicircum{}",
	)
	return r.Replace(s)
}

func writeFile(path, content string) {
	if err := os.WriteFile(path, []byte(content), 0o644); err != nil {
		fatal(err)
	}
}

func fatal(err error) {
	_, _ = fmt.Fprintf(os.Stderr, "error: %v\n", err)
	os.Exit(1)
}
